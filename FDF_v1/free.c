#include "fdf.h"

int			free_z(t_fdf *fdf, int i)
{
	int c;

	c = 0;
	while (c < i)
	{
		free(fdf->z_matrix[c]);
		++c;
	}
	free(fdf->z_matrix);
	return (0);
}
